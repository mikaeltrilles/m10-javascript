/*
Ecrivez une fonction qui prend 2 nombres a et b en paramètres et retourne le résultat 
suivant : a * b + a + b
*/

function calcul(a, b) {
  return a * b + a + b;
}

// Appel de la fonction avec les paramètres 2.5 et 8
console.log(calcul(2.5, 8));

// Appel de la fonction avec les paramètres a et bc
console.log(calcul("a", "bc"));